<script>
    function createPagination(currentPage, totalPages) {
    const pagination = document.getElementById('pagination');
    pagination.innerHTML = ''; // Clear existing pagination

    // Add the "Previous" button
    const prevButton = document.createElement('li');
    prevButton.className = `page-item ${currentPage === 1 ? 'disabled' : ''}`;
    prevButton.innerHTML = ` 
        <a class="page-link" href="#" aria-label="Previous" onclick="goToPage(${currentPage - 1})">
            <span class="fas fa-arrow-circle-left"></span>
            <span class="sr-only">Previous</span>
        </a>`;
    pagination.appendChild(prevButton);

    // Add page numbers
    for (let i = 1; i <= totalPages; i++) {
        if (i === 1 || i === totalPages || Math.abs(i - currentPage) <= 1) {
            const pageItem = document.createElement('li');
            pageItem.className = `page-item ${i === currentPage ? 'active' : ''}`;
            pageItem.innerHTML = ` 
                <a class="page-link" href="#" onclick="goToPage(${i})">${i}</a>`;
            pagination.appendChild(pageItem);
        } else if (i === currentPage - 2 || i === currentPage + 2) {
            // Add ellipsis
            const ellipsis = document.createElement('li');
            ellipsis.className = 'page-item disabled';
            ellipsis.innerHTML = `<a class="page-link">...</a>`;
            pagination.appendChild(ellipsis);
        }
    }

    // Add the "Next" button
    const nextButton = document.createElement('li');
    nextButton.className = `page-item ${currentPage === totalPages ? 'disabled' : ''}`;
    nextButton.innerHTML = `
        <a class="page-link" href="#" aria-label="Next" onclick="goToPage(${currentPage + 1})">
            <span class="fas fa-arrow-circle-right"></span>
            <span class="sr-only">Next</span>
        </a>`;
    pagination.appendChild(nextButton);
}

function goToPage(page) {
    const cardsPerPage = 4; // Adjust this as needed
    const allCards = document.querySelectorAll('.grouping-listings-single');
    const totalCards = allCards.length;
    const totalPages = Math.ceil(totalCards / cardsPerPage);

    if (page < 1 || page > totalPages) return; // Don't go beyond valid pages

    // Hide all cards initially
    allCards.forEach(card => card.style.display = 'none');

    // Show the cards for the current page
    const startIndex = (page - 1) * cardsPerPage;
    const endIndex = Math.min(page * cardsPerPage, totalCards);

    for (let i = startIndex; i < endIndex; i++) {
        allCards[i].style.display = 'block'; // Show card
    }

    // Update pagination
    createPagination(page, totalPages);
}

// Initialize pagination with the first page
document.addEventListener("DOMContentLoaded", () => {
    goToPage(1); // Start with the first page
});

